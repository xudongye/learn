public class PictureOperationBo {
    private Double x;

    private Double y;

    private Double w;

    private Double h;

    private Integer rotateDegree;

    public Double getX() {
        return x;
    }

    public void setX(Double x) {
        this.x = x;
    }

    public Double getY() {
        return y;
    }

    public void setY(Double y) {
        this.y = y;
    }

    public Double getW() {
        return w;
    }

    public void setW(Double w) {
        this.w = w;
    }

    public Double getH() {
        return h;
    }

    public void setH(Double h) {
        this.h = h;
    }

    public Integer getRotateDegree() {
        return rotateDegree;
    }

    public void setRotateDegree(Integer rotateDegree) {
        this.rotateDegree = rotateDegree;
    }
}
